-- =============================================
-- 7. DOSYALAR TABLOSU (File Management)
-- =============================================
PROMPT 7. DOSYALAR tablosu oluşturuluyor...

CREATE TABLE DOSYALAR (
    ID NUMBER(10) DEFAULT SEQ_DOSYALAR.NEXTVAL PRIMARY KEY,
    DUYURU_ID NUMBER(10), -- NULL olabilir (önce dosya yüklenip sonra duyuruya bağlanabilir)
    YUKLEYEN_KULLANICI_ID NUMBER(10) NOT NULL,
    SESSION_ID VARCHAR2(100), -- Geçici dosyalar için oturum ID'si
    DOSYA_ADI VARCHAR2(255) NOT NULL, -- Orijinal dosya adı
    DOSYA_YOLU VARCHAR2(500) NOT NULL, -- Sunucudaki fiziksel path
    DOSYA_TIPI VARCHAR2(100) NOT NULL, -- MIME type (image/jpeg, application/pdf, etc.)
    DOSYA_KATEGORISI VARCHAR2(50) DEFAULT 'ATTACHMENT' NOT NULL, -- ATTACHMENT, INLINE_IMAGE, BANNER
    DOSYA_BOYUTU NUMBER(12) NOT NULL, -- Byte cinsinden dosya boyutu
    DOSYA_HASH VARCHAR2(64), -- Dosya bütünlüğü kontrolü için MD5/SHA hash
    ACIKLAMA VARCHAR2(500), -- Dosya açıklaması
    AKTIF CHAR(1) DEFAULT 'Y' NOT NULL, -- 'Y' veya 'N'
    YUKLEME_TARIHI DATE DEFAULT SYSDATE NOT NULL,
    GUNCELLEME_TARIHI DATE,
    CONSTRAINT FK_DOSYALAR_YUKLEYEN FOREIGN KEY (YUKLEYEN_KULLANICI_ID) REFERENCES KULLANICILAR(ID),
    CONSTRAINT CK_DOSYALAR_AKTIF CHECK (AKTIF IN ('Y', 'N')),
    CONSTRAINT CK_DOSYA_KATEGORISI CHECK (DOSYA_KATEGORISI IN ('ATTACHMENT', 'INLINE_IMAGE', 'BANNER')),
    CONSTRAINT UK_DOSYALAR_DUYURU_HASH UNIQUE (DUYURU_ID, DOSYA_HASH)
);

-- =============================================
-- 8. EPOSTA DUYURULARI TABLOSU
-- =============================================
PROMPT 8. EPOSTA_DUYURULARI tablosu oluşturuluyor...

CREATE TABLE EPOSTA_DUYURULARI (
    ID NUMBER(10) DEFAULT SEQ_EPOSTA_DUYURULARI.NEXTVAL PRIMARY KEY,
    KONU VARCHAR2(500) NOT NULL,
    ICERIK CLOB NOT NULL, -- HTML içerik (email) veya Plain Text (sosyal medya)
    ACIKLAMA VARCHAR2(1000), -- Duyuru açıklaması (EDITOR tarafından doldurulur, Kontrolör/manager için bilgi)
    ICERIK_TIPI VARCHAR2(30) DEFAULT 'EMAIL' NOT NULL, -- EMAIL, SOSYAL_MEDYA_X, SOSYAL_MEDYA_FACEBOOK, SOSYAL_MEDYA_INSTAGRAM
    DUYURU_KATEGORISI VARCHAR2(50) DEFAULT 'GENEL_DUYURU_IMZASIZ' NOT NULL, -- Duyuruya imza eklemek için kategori
    GONDERICI_KATEGORI VARCHAR2(50) DEFAULT 'EMAIL_DUYURU' NOT NULL, -- SMTP gönderici kategorisi (EMAIL_DUYURU, EMAIL_REKTOR, etc.) - Hangi email hesabından gönderileceğini belirler
    DURUM VARCHAR2(30) DEFAULT 'TASLAK' NOT NULL, -- TASLAK, ILK_ONAY_BEKLIYOR, SON_ONAY_BEKLIYOR, REDDEDILDI, ONAYLANDI, GONDERILDI, IPTAL
    OLUSTURAN_KULLANICI_ID NUMBER(10) NOT NULL,
    ILK_ONAYLAYAN_KULLANICI_ID NUMBER(10), -- Koordinatör (ilk onaylayan)
    SON_ONAYLAYAN_KULLANICI_ID NUMBER(10), -- Manager (son onaylayan)    
    SABLON_ID NUMBER(10),
    BANNER_DOSYA_ID NUMBER(10), -- Ana banner/resim dosyası
    GERCEK_GONDERIM_TARIHI DATE, -- Gerçek gönderim tarihi (sadece EMAIL için)
    TOPLAM_ALICI_SAYISI NUMBER(8) DEFAULT 0, -- Sadece EMAIL için
    BASARILI_GONDERIM_SAYISI NUMBER(8) DEFAULT 0, -- Sadece EMAIL için
    BASARISIZ_GONDERIM_SAYISI NUMBER(8) DEFAULT 0, -- Sadece EMAIL için
    OLUSTURMA_TARIHI DATE DEFAULT SYSDATE NOT NULL,
    GUNCELLEME_TARIHI DATE,
    CONSTRAINT FK_DUYURU_SABLON FOREIGN KEY (SABLON_ID) REFERENCES EPOSTA_SABLONLARI(ID),
    CONSTRAINT FK_DUYURU_OLUSTURAN FOREIGN KEY (OLUSTURAN_KULLANICI_ID) REFERENCES KULLANICILAR(ID),
    CONSTRAINT FK_DUYURU_ILK_ONAYLAYAN FOREIGN KEY (ILK_ONAYLAYAN_KULLANICI_ID) REFERENCES KULLANICILAR(ID),
    CONSTRAINT FK_DUYURU_SON_ONAYLAYAN FOREIGN KEY (SON_ONAYLAYAN_KULLANICI_ID) REFERENCES KULLANICILAR(ID),
    CONSTRAINT FK_DUYURU_BANNER FOREIGN KEY (BANNER_DOSYA_ID) REFERENCES DOSYALAR(ID) ON DELETE SET NULL,
    CONSTRAINT CK_DUYURU_DURUM CHECK (DURUM IN ('TASLAK', 'ILK_ONAY_BEKLIYOR', 'SON_ONAY_BEKLIYOR', 'REDDEDILDI', 'ONAYLANDI', 'GONDERILIYOR', 'GONDERILDI', 'IPTAL')),
    CONSTRAINT CK_DUYURU_ICERIK_TIPI CHECK (ICERIK_TIPI IN ('EMAIL', 'SOSYAL_MEDYA'))
);

