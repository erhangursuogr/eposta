-- 1. ROLLER TABLOSU
-- =============================================
PROMPT 1. ROLLER tablosu oluşturuluyor...

CREATE TABLE ROLLER (
    ID NUMBER(5) DEFAULT SEQ_ROLLER.NEXTVAL PRIMARY KEY,
    ROL_KODU VARCHAR2(20) NOT NULL,
    ROL_ADI VARCHAR2(100) NOT NULL,
    ACIKLAMA VARCHAR2(500),
    YETKI_SEVIYESI NUMBER(2) NOT NULL, -- 1-10 arası, yüksek numara = daha fazla yetki
    AKTIF CHAR(1) DEFAULT 'Y' NOT NULL, -- 'Y' veya 'N'
    OLUSTURMA_TARIHI DATE DEFAULT SYSDATE NOT NULL,
    GUNCELLEME_TARIHI DATE,
    CONSTRAINT UK_ROLLER_KOD UNIQUE (ROL_KODU),
    CONSTRAINT CK_ROLLER_AKTIF CHECK (AKTIF IN ('Y', 'N')),
    CONSTRAINT CK_ROLLER_YETKI CHECK (YETKI_SEVIYESI BETWEEN 1 AND 10)
);

-- =============================================
-- 2. KULLANICILAR TABLOSU
-- =============================================
PROMPT 2. KULLANICILAR tablosu oluşturuluyor...

CREATE TABLE KULLANICILAR (
    ID NUMBER(10) DEFAULT SEQ_KULLANICILAR.NEXTVAL PRIMARY KEY,
    ROL_ID NUMBER(5) NOT NULL, -- Foreign Key to ROLLER table
    KULLANICI_ADI VARCHAR2(100) NOT NULL,
    AD_SOYAD VARCHAR2(200) NOT NULL,
    EMAIL VARCHAR2(200) NOT NULL,
    DEPARTMAN VARCHAR2(100),
    UNVAN VARCHAR2(100),
    GOREV_YERI NUMBER(10) DEFAULT 0,
    GOREV_YERI_ADI VARCHAR2(200) DEFAULT 'REKTÖRLÜK', -- Görev yeri adı
    AKTIF CHAR(1) DEFAULT 'Y' NOT NULL, -- 'Y' veya 'N'
    SON_GIRIS_TARIHI DATE,
    OLUSTURMA_TARIHI DATE DEFAULT SYSDATE NOT NULL,
    GUNCELLEME_TARIHI DATE,
    CONSTRAINT UK_KULLANICILAR_EMAIL UNIQUE (EMAIL),
    CONSTRAINT UK_KULLANICILAR_KULLANICI_ADI UNIQUE (KULLANICI_ADI),
    CONSTRAINT FK_KULLANICILAR_ROL FOREIGN KEY (ROL_ID) REFERENCES ROLLER(ID),
    CONSTRAINT CK_KULLANICILAR_AKTIF CHECK (AKTIF IN ('Y', 'N'))
);

