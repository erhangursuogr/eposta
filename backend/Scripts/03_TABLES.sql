-- 10. EPOSTA DUYURU ALICILARI TABLOSU
-- =============================================
PROMPT 10. EPOSTA_DUYURU_ALICILARI tablosu oluşturuluyor...

CREATE TABLE EPOSTA_DUYURU_ALICILARI (
    ID NUMBER(10) DEFAULT SEQ_EPOSTA_DUYURU_ALICILARI.NEXTVAL PRIMARY KEY,
    DUYURU_ID NUMBER(10) NOT NULL,
    GRUP_ID NUMBER(10), -- NULL ise manuel email
    ALICI_TIPI VARCHAR2(20) DEFAULT 'GRUP' NOT NULL, -- 'GRUP' veya 'MANUEL'
    ALICI_KATEGORISI VARCHAR2(10) DEFAULT 'BCC' NOT NULL, -- 'TO', 'CC', 'BCC'
    EMAIL VARCHAR2(200), -- GRUP için NULL olabilir, MANUEL için zorunlu
    AD_SOYAD VARCHAR2(200),
    GONDERIM_DURUMU VARCHAR2(20) DEFAULT 'BEKLIYOR' NOT NULL, -- 'BEKLIYOR', 'GONDERILDI', 'BASARISIZ', 'IPTAL'
    HATA_MESAJI VARCHAR2(500),
    GONDERIM_TARIHI DATE,
    OLUSTURMA_TARIHI DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_DUYURU_ALICI_DUYURU FOREIGN KEY (DUYURU_ID) REFERENCES EPOSTA_DUYURULARI(ID) ON DELETE CASCADE,
    CONSTRAINT FK_DUYURU_ALICI_GRUP FOREIGN KEY (GRUP_ID) REFERENCES EPOSTA_GRUPLARI(ID),
    CONSTRAINT CK_DUYURU_ALICI_TIPI CHECK (ALICI_TIPI IN ('GRUP', 'MANUEL')),
    CONSTRAINT CK_DUYURU_ALICI_KATEGORISI CHECK (ALICI_KATEGORISI IN ('TO', 'CC', 'BCC')),
    CONSTRAINT CK_DUYURU_GONDERIM_DURUMU CHECK (GONDERIM_DURUMU IN ('BEKLIYOR', 'GONDERILDI', 'BASARISIZ', 'IPTAL')),
    -- Alıcı tipi validasyonu: GRUP için GRUP_ID gerekli ve EMAIL NULL, MANUEL için EMAIL gerekli ve GRUP_ID NULL
    CONSTRAINT CK_Recipient_Type_Valid CHECK (
        (ALICI_TIPI = 'GRUP' AND GRUP_ID IS NOT NULL AND EMAIL IS NULL) OR
        (ALICI_TIPI = 'MANUEL' AND GRUP_ID IS NULL AND EMAIL IS NOT NULL)
    )
);

